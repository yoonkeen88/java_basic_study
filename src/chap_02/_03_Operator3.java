package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        System.out.println(5>3); // -> boolean
        System.out.println(5>=3);
        System.out.println(5>=5);
        System.out.println(5>=6);

        System.out.println();
        System.out.println(5<=5);
        System.out.println(5<=6);

        System.out.println();
        System.out.println(5==5);
        System.out.println(5==3);

        System.out.println();
        System.out.println(5!=5);
        System.out.println(5!=3);
    }
}
